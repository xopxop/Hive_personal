int	max(int *tab, unsigned int len)
{
	int nb;

	if (len == 0 || !*tab)
		return (0);
	nb = tab[len - 1];
	while (len > 0)
	{
		if (tab[len - 1] > nb)
			nb = tab[len - 1];
		len--;
	}
	return (nb);
}
